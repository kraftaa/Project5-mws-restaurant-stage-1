/**
* Adding basic service worker to be able to use the app offline
* using instructions from https://developers.google.com/web/ilt/pwa/introduction-to-service-worker
* at first we are checking if browser has service worker, then we are setting it up
* and providing messages if SW is registered or registration failed
*/
/* need to register service worker file in main directory to make the whole scope accessible */
if ('serviceWorker' in navigator) {
  navigator.serviceWorker
  .register('/service-worker.js')
  .then(registration => {
    console.log('Registration successful, scope is:', registration.scope);
  }).catch(error => {
    console.log('Service worker registration failed, error:', error);
  });
}

